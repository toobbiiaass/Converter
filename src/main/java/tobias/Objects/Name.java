package tobias.Objects;

public class Name {
   private String oldName;
   private String newName;

    public Name(String oldName, String newName) {
        this.oldName = oldName;
        this.newName = newName;
    }

    public String getOldName() {
        return oldName;
    }

    public String getNewName() {
        return newName;
    }
}
